package pepository.exercise.java;

public class T03 {
    public static void main(String[] args) {
        //自增自减测试
        int a = (2);
        System.out.println(a++); // 2
        int b = (2);
        System.out.println(b--); // 2
        int c = (2);
        c++;
        System.out.println(c);
        int d = (2);
        d--;
        System.out.println(d);
    }
}
